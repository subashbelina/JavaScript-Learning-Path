/*
  Arithmetic Operators (7 operators)
  --------------------------------
  Used for math calculations on numbers.
  Operators: +  -  *  /  %  **  ++  --
*/

let a = 10;
let b = 3;

console.log("--- Arithmetic Operators ---");
console.log(a + b);   // 13  — addition
console.log(a - b);   // 7   — subtraction
console.log(a * b);   // 30  — multiplication
console.log(a / b);   // 3.33... — division
console.log(a % b);   // 1   — remainder (modulus)
console.log(a ** b);  // 1000 — exponent (10 to the power 3)
console.log(++a);     // 11  — increment (add 1)
console.log(--b);     // 2   — decrement (subtract 1)
