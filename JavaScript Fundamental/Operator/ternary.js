/*
  Ternary Operator (1 operator)
  -------------------------------
  A short way to write if/else in one line.
  Syntax: condition ? valueIfTrue : valueIfFalse
*/

let userAge = 20;
let status = userAge >= 18 ? "Adult" : "Minor";

console.log("--- Ternary Operator ---");
console.log(status); // "Adult"
